package Session12;

import java.util.Vector;

public class Vectorr {
public static void main(String[] args) {
	Vector<Integer> v = new Vector<Integer>();
	v.add(0);
	v.add(2);
	v.add(4);
	System.out.println("Adding Elements "+v);
	v.remove(1);
	System.out.println("Removing second element "+v);
	v.set(0, 2);
	System.out.println("Changing the value of first element- "+v);
	System.out.println("Checking if Element is present- "+v.contains(4));
}
}
























































































































































